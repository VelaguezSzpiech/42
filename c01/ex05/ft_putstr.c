#include <unistd.h>

void	ft_putstr(char *str)
{
	while (*str)
	{
		write(1, str, 1);
		str++;
	}
}

int main(void)
{
	char STR[12]="hello world";
	ft_putstr(STR);
	
	return 0;
}
